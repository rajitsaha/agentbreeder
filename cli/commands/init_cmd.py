"""{name} — CrewAI agent.

A simple crew with a researcher and writer agent.
"""

from crewai import Agent, Crew, Task


researcher = Agent(
    role="Researcher",
    goal="Find accurate, up-to-date information",
    backstory="You are a meticulous researcher who verifies every fact.",
    verbose=True,
)

writer = Agent(
    role="Writer",
    goal="Create clear, concise summaries",
    backstory="You turn complex research into readable prose.",
    verbose=True,
)


