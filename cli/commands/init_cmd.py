def _agent_py_claude_sdk(name: str) -> str:
    return f'''\
"""{name} — Claude SDK agent.

A simple tool-using agent powered by Anthropic's Claude.
"""

import anthropic


client = anthropic.Anthropic()

tools = [
    {{
        "name": "get_weather",
        "description": "Get current weather for a city.",
        "input_schema": {{
            "type": "object",
            "properties": {{
                "city": {{"type": "string", "description": "City name"}},
            }},
            "required": ["city"],
        }},
    }}
]


def get_weather(city: str) -> str:
    """Stub tool — replace with real implementation."""
    return f"Sunny, 22°C in {{city}}"


def run(user_message: str = "What's the weather in Tokyo?") -> str:
    """Run the agent with a user message."""
    response = client.messages.create(
        model="claude-sonnet-4-20250514",
        max_tokens=1024,
        tools=tools,
        messages=[{{"role": "user", "content": user_message}}],
    )

    # Handle tool use
    for block in response.content:
        if block.type == "tool_use" and block.name == "get_weather":
            tool_result = get_weather(block.input["city"])
            # Send tool result back
            follow_up = client.messages.create(
                model="claude-sonnet-4-20250514",
                max_tokens=1024,
                tools=tools,
                messages=[
                    {{"role": "user", "content": user_message}},
                    {{"role": "assistant", "content": response.content}},
                    {{"role": "user", "content": [
                        {{"type": "tool_result", "tool_use_id": block.id, "content": tool_result}}
                    ]}},
                ],
            )
            return follow_up.content[0].text

    return response.content[0].text


if __name__ == "__main__":
    print(run())
'''


