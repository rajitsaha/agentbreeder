# ─── Selection UI helpers ────────────────────────────────────────────


def _show_selection(title: str, items: list[dict], selected: int) -> None:
    """Render a selection menu with the selected item highlighted."""
    table = Table(
        show_header=False,
        show_edge=False,
        box=None,
        padding=(0, 2),
    )
    table.add_column(width=3)
    table.add_column(width=3)
    table.add_column(width=20)
    table.add_column()

    for i, item in enumerate(items):
        num = str(i + 1)
        if i == selected:
            table.add_row(
                "[bold cyan]›[/bold cyan]",
                item["icon"],
                f"[bold cyan]{item['name']}[/bold cyan]",
                f"[dim cyan]{item['tagline']}[/dim cyan]",
            )
        else:
            table.add_row(
                f"[dim]{num}[/dim]",
                item["icon"],
                item["name"],
                f"[dim]{item['tagline']}[/dim]",
            )

    console.print(f"\n  [bold]{title}[/bold]\n")
    console.print(table)


def _prompt_selection(title: str, items: list[dict]) -> dict:
    """Prompt the user to select from a numbered list."""
    _show_selection(title, items, selected=-1)

    while True:
        console.print()
        raw = console.input(f"  [bold]Enter number (1-{len(items)}): [/bold]")
        try:
            choice = int(raw.strip())
            if 1 <= choice <= len(items):
                selected = items[choice - 1]
                console.print(f"  [green]✓[/green] {selected['icon']} {selected['name']}")
                return selected
        except ValueError:
            pass
        console.print(f"  [red]Please enter a number between 1 and {len(items)}[/red]")


def _prompt_text(
    label: str,
    default: str = "",
    validate_fn: Callable[..., Any] | None = None,
) -> str:
    """Prompt for text input with optional validation."""
    while True:
        if default:
            raw = console.input(f"  [bold]{label}[/bold] [dim]({default})[/dim]: ")
            value = raw.strip() or default
        else:
            raw = console.input(f"  [bold]{label}[/bold]: ")
            value = raw.strip()

        if not value:
            console.print("  [red]This field is required[/red]")
            continue

        if validate_fn:
            error = validate_fn(value)
            if error:
                console.print(f"  [red]{error}[/red]")
                continue

        console.print(f"  [green]✓[/green] {value}")
        return value


def _validate_agent_name(name: str) -> str | None:
    """Validate an agent name. Returns error message or None."""
    if len(name) < 2:
        return "Name must be at least 2 characters"
    if not re.match(r"^[a-z0-9][a-z0-9-]*[a-z0-9]$", name):
        return "Must be lowercase alphanumeric with hyphens (e.g., 'my-agent')"
    return None


def _validate_email(email: str) -> str | None:
    """Basic email validation. Returns error message or None."""
    if "@" not in email or "." not in email.split("@")[-1]:
        return "Please enter a valid email address"
    return None


# ─── Main command ────────────────────────────────────────────────────


def init(
    output_dir: str = typer.Argument(
        None,
        help="Directory to create the project in (defaults to agent name)",
    ),
    json_output: bool = typer.Option(
        False,
        "--json",
        help="Output as JSON (for CI/scripting)",
    ),
) -> None:
    """Scaffold a new agent project with an interactive wizard.

    Creates a complete, deployable agent project with:
      - agent.yaml (configuration)
      - agent.py (working example code)
      - requirements.txt (dependencies)
      - .env.example (environment template)
      - README.md (getting started guide)
    """
    if not json_output:
        console.print()
        console.print(
            Panel(
                "[bold]New Agent Project[/bold]\n"
                "[dim]Answer a few questions and you'll have a deployable agent in seconds.[/dim]",
                title="[bold]AgentBreeder[/bold]",
                border_style="blue",
                padding=(1, 2),
            )
        )

    # ── Step 1: Framework ────────────────────────────────────────
    framework = _prompt_selection("What framework?", FRAMEWORKS)

    # ── Step 2: Cloud ────────────────────────────────────────────
    cloud = _prompt_selection("Where will it run?", CLOUDS)

    # ── Step 3: Details ──────────────────────────────────────────
    console.print("\n  [bold]Project details[/bold]\n")

    cwd_name = Path.cwd().name
    default_name = re.sub(r"[^a-z0-9-]", "-", cwd_name.lower()).strip("-") or "my-agent"
    if len(default_name) < 2:
        default_name = "my-agent"

    name = _prompt_text("Agent name", default=default_name, validate_fn=_validate_agent_name)
    team = _prompt_text("Team", default="engineering")

    # Try to get a sensible email default from git config
    git_email = ""
    try:
        import subprocess

        result = subprocess.run(
            ["git", "config", "user.email"],
            capture_output=True,
            text=True,
            timeout=5,
        )
        if result.returncode == 0:
            git_email = result.stdout.strip()
    except Exception:
        pass

    owner = _prompt_text("Owner email", default=git_email, validate_fn=_validate_email)

    # ── Step 4: Generate files ───────────────────────────────────
    project_dir = Path(output_dir) if output_dir else Path.cwd() / name
    project_dir = project_dir.resolve()

    if project_dir.exists() and any(project_dir.iterdir()):
        console.print(
            f"\n  [yellow]Warning:[/yellow] Directory [bold]{project_dir}[/bold] is not empty."
        )
        overwrite = console.input("  [bold]Continue anyway? (y/N): [/bold]").strip().lower()
        if overwrite != "y":
            console.print("  [dim]Aborted.[/dim]")
            raise typer.Exit(code=0)

    project_dir.mkdir(parents=True, exist_ok=True)

    files = {
        "agent.yaml": _agent_yaml(
            name=name,
            framework=framework["key"],
            cloud=cloud["key"],
            model=framework["model"],
            team=team,
            owner=owner,
        ),
        "agent.py": AGENT_PY_GENERATORS[framework["key"]](name),
        "requirements.txt": _requirements(framework["key"]),
        ".env.example": _env_example(framework["key"]),
        "README.md": _readme(name, framework["key"], cloud["key"]),
    }

    if not json_output:
        console.print()
        console.print(Rule(style="dim"))
        console.print()

    created_files = []
    for filename, content in files.items():
        filepath = project_dir / filename
        filepath.write_text(content)
        created_files.append(str(filepath))
        if not json_output:
            console.print(f"  [green]✓[/green] {filename}")

    # ── Step 5: Validate ─────────────────────────────────────────
    yaml_path = project_dir / "agent.yaml"
    validation = validate_config(yaml_path)

    if json_output:
        import json
        import sys

        output = {
            "project_dir": str(project_dir),
            "name": name,
            "framework": framework["key"],
            "cloud": cloud["key"],
            "team": team,
            "owner": owner,
            "files": created_files,
            "valid": validation.valid,
        }
        if not validation.valid:
            output["validation_errors"] = [e.model_dump() for e in validation.errors]
        sys.stdout.write(json.dumps(output, indent=2) + "\n")
        return

    console.print()

    if validation.valid:
        console.print("  [green]✓[/green] agent.yaml validated")
    else:
        console.print("  [yellow]⚠[/yellow] agent.yaml has validation warnings:")
        for err in validation.errors:
            console.print(f"    [dim]- {err.path}: {err.message}[/dim]")

    # ── Step 6: Next steps ───────────────────────────────────────
    rel_dir = os.path.relpath(project_dir, Path.cwd())
    # Use relative path if simple (no ..), otherwise use absolute
    if rel_dir == ".":
        cd_cmd = ""
    elif rel_dir.startswith(".."):
        cd_cmd = f"cd {project_dir}\n  "
    else:
        cd_cmd = f"cd {rel_dir}\n  "

    console.print()
    console.print(
        Panel(
            f"[bold green]Project created![/bold green] {framework['icon']} {name}\n\n"
            f"  [bold]Next steps:[/bold]\n\n"
            f"  [dim]$[/dim] {cd_cmd}pip install -r requirements.txt\n"
            f"  [dim]$[/dim] cp .env.example .env\n"
            f"  [dim]$[/dim] [dim]# Add your API keys to .env[/dim]\n"
            f"  [dim]$[/dim] python agent.py\n"
            f"  [dim]$[/dim] agentbreeder validate agent.yaml\n"
            f"  [dim]$[/dim] agentbreeder deploy agent.yaml --target {cloud['key']}",
            title="Ready",
            border_style="green",
            padding=(1, 2),
        )
    )
    console.print()
